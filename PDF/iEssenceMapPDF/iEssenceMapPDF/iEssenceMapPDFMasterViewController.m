- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    //if ([[UIDevice currentDevice] userInterfaceIdiom] == UIUserInterfaceIdiomPhone) {

//	    iEssenceMapPDFDetailViewController *detailViewController = [[[iEssenceMapPDFDetailViewController alloc] initWithNibName:@"iEssenceMapPDFDetailViewController_iPhone" bundle:nil] autorelease];
//        detailViewController.fileIndex = indexPath.row;
//        [self.navigationController pushViewController:detailViewController animated:YES];
        
        NSString *fileName = [[[FilesHelper sharedFileHelper] pdfFiles] objectAtIndex:indexPath.row];
        NSString *path = [FilesHelper documentsPathWithFileName:fileName];
        ReaderDocument *document = [ReaderDocument withDocumentFilePath:path password:nil];
        
        if (document != nil) // Must have a valid ReaderDocument object in order to proceed with things
        {
            ReaderViewController *readerViewController = [[ReaderViewController alloc] initWithReaderDocument:document];
            
            readerViewController.delegate = self; // Set the ReaderViewController delegate to self
            
#if (DEMO_VIEW_CONTROLLER_PUSH == TRUE)
            
            [self.navigationController pushViewController:readerViewController animated:YES];
            
#else // present in a modal view controller
            
            readerViewController.modalTransitionStyle = UIModalTransitionStyleCrossDissolve;
            readerViewController.modalPresentationStyle = UIModalPresentationFullScreen;
            
            [self presentModalViewController:readerViewController animated:YES];
            
#endif // DEMO_VIEW_CONTROLLER_PUSH
            
            [readerViewController release]; // Release the ReaderViewController
        }
    //}
}
